public class typecasting {
    public static void main(String[] args) {
        // widening
        int myNum = 9;
        double mynum = myNum;
        System.out.println(myNum);
        System.out.println(mynum);
        // narrowing

        float myNumber = 10.11f;
        int Mynumber = (int) myNumber;
        System.out.println(myNumber);
        System.out.println(Mynumber);

    }
}
